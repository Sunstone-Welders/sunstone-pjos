// Stub implementation used when SquareMobilePaymentsSDK is not linked into
// the target. Lets the project compile on machines where the SDK hasn't been
// added yet; every method rejects with a clear error.

@objc(SquareTapToPayPlugin)
public class SquareTapToPayPlugin: CAPPlugin {

    private static let notInstalledMessage =
        "Square Mobile Payments SDK not installed"

    @objc func initialize(_ call: CAPPluginCall) {
        call.reject(Self.notInstalledMessage)
    }

    @objc func isAvailable(_ call: CAPPluginCall) {
        call.reject(Self.notInstalledMessage)
    }

    @objc func getAuthorizationState(_ call: CAPPluginCall) {
        call.reject(Self.notInstalledMessage)
    }

    @objc func startPayment(_ call: CAPPluginCall) {
        call.reject(Self.notInstalledMessage)
    }
}

