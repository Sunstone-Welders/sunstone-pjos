// ============================================================================
// SquareTapToPayPlugin — Capacitor bridge to Square's Mobile Payments SDK
// ios/App/App/Plugins/SquareTapToPay/SquareTapToPayPlugin.swift
// ============================================================================
// Exposes 5 methods to the web layer:
//   initialize, isAvailable, startPayment, getAuthorizationState,
//   presentSettings
//
// SDK reference:
//   https://developer.squareup.com/docs/mobile-payments-sdk/ios
//
// The SDK is added to the App target via Swift Package Manager — see
// SETUP_NATIVE.md for the exact Xcode steps. Add the package URL
// https://github.com/square/mobile-payments-sdk-ios @ 2.5.0 to the App target
// (not the CapApp-SPM module, whose Package.swift is regenerated by `cap sync`).
//
// When the SDK is NOT linked into the target, every method rejects with a
// clear error so the project still builds on machines without it configured.
// ============================================================================

import Capacitor
import CoreLocation
import Foundation
import UIKit

#if canImport(SquareMobilePaymentsSDK)
import SquareMobilePaymentsSDK

@objc(SquareTapToPayPlugin)
public class SquareTapToPayPlugin: CAPPlugin {

    /// Currently in-flight payment call. The Square SDK reports completion via
    /// the PaymentManagerDelegate, so we hold the call here until a delegate
    /// callback fires and resolves/rejects it.
    private var pendingPaymentCall: CAPPluginCall?
    private var paymentHandle: PaymentHandle?
    private var paymentDelegate: PaymentDelegateBridge?

    /// Tracks whether `MobilePaymentsSDK.initialize(...)` has been called.
    /// Accessing `MobilePaymentsSDK.shared` before this is true is a fatal
    /// error inside the SDK — every public method below must check this flag.
    private var isInitialized = false

    /// Square's Mobile Payments SDK requires CoreLocation authorization before
    /// processing any Tap to Pay transaction. We hold the manager + delegate
    /// here so they outlive the async `requestWhenInUseAuthorization` flow.
    private var locationManager: CLLocationManager?
    private var locationDelegate: LocationDelegateBridge?

    /// Held strongly so the SDK's weakly-referenced observer stays alive for
    /// the lifetime of the plugin. Registered once after authorization.
    private var readerObserver: ReaderObserverBridge?

    /// One-shot callback fired the next time a Tap to Pay reader connects.
    /// Used by startPayment() to wait for reader pairing (up to 10s) before
    /// handing off to Square's UI.
    private var readerWaitCompletion: ((Bool) -> Void)?

    /// Square's ReaderModel rawValue for the embedded Tap to Pay on iPhone
    /// reader. Stored as an Int constant so the wait helper can match without
    /// importing the enum case directly (rawValue is stable across SDK minor
    /// versions; the enum case name has shifted).
    private static let tapToPayReaderModelRawValue = 3

    private static let locationDeniedMessage =
        "Location access is required for Tap to Pay. Please enable it in Settings > Privacy > Location Services."

    // ── initialize ──────────────────────────────────────────────────────────

    @objc func initialize(_ call: CAPPluginCall) {
        print("SquareTapToPay: initialize called")
        guard let applicationId = call.getString("squareApplicationID"),
              let accessToken = call.getString("accessToken"),
              let locationId = call.getString("locationId") else {
            call.reject("squareApplicationID, accessToken, and locationId are required")
            return
        }

        // Square's SDK touches UIKit (UIApplication.connectedScenes,
        // UIWindowScene.interfaceOrientation, etc.) during initialize() and
        // authorize(). Capacitor dispatches plugin methods on a background
        // "bridge" queue, so every Square SDK call must hop to main.
        DispatchQueue.main.async {
            // Bring the SDK up before touching `.shared` for the first time.
            // Safe to call once per process; guard with isInitialized.
            if !self.isInitialized {
                MobilePaymentsSDK.initialize(
                    applicationLaunchOptions: nil,
                    squareApplicationID: applicationId
                )
                self.isInitialized = true
                print("SquareTapToPay: SDK initialized")
            }

            let authState = MobilePaymentsSDK.shared.authorizationManager.state
            print("SquareTapToPay: auth state = \(authState)")

            // Calling authorize() again after a successful auth fails with
            // "authorization_already_authorized". Check state first and skip.
            if authState == .authorized {
                print("SquareTapToPay: already authorized, skipping")
                self.completeInitialize(call: call)
                return
            }

            print("SquareTapToPay: calling authorize with locationId=\(locationId)")
            MobilePaymentsSDK.shared.authorizationManager.authorize(
                withAccessToken: accessToken,
                locationID: locationId
            ) { error in
                if let error = error {
                    let ns = error as NSError
                    print("SquareTapToPay: authorize error domain=\(ns.domain) code=\(ns.code) userInfo=\(ns.userInfo)")

                    // Re-check state on the main thread — the SDK may have
                    // transitioned to .authorized even when reporting an error
                    // ("already_authorized" is the canonical case).
                    DispatchQueue.main.async {
                        let postState = MobilePaymentsSDK.shared.authorizationManager.state
                        let errorString = "\(error)"
                        let userInfoString = "\(ns.userInfo)"
                        let alreadyAuthorized =
                            ns.userInfo["code"] as? String == "authorization_already_authorized" ||
                            errorString.contains("already_authorized") ||
                            userInfoString.contains("already_authorized") ||
                            ns.localizedDescription.lowercased().contains("already_authorized") ||
                            ns.localizedDescription.lowercased().contains("already authorized") ||
                            postState == .authorized

                        if alreadyAuthorized {
                            print("SquareTapToPay: treating already_authorized as success")
                            self.isInitialized = true
                            self.completeInitialize(call: call)
                            return
                        }

                        call.reject(
                            "Square authorization failed: \(ns.localizedDescription)",
                            "\(ns.code)",
                            error,
                            [
                                "message": ns.localizedDescription,
                                "code": ns.code,
                                "domain": ns.domain,
                                "userInfo": Self.stringifyUserInfo(ns.userInfo)
                            ]
                        )
                    }
                    return
                }
                DispatchQueue.main.async {
                    self.completeInitialize(call: call)
                }
            }
        }
    }

    /// Finishes the initialize() flow after Square authorization succeeds.
    /// Waits for CoreLocation permission to resolve BEFORE resolving the JS
    /// promise — by the time the user taps "Tap to Pay" the OS prompt will
    /// already be dismissed and CLLocationManager.authorizationStatus will be
    /// stable. Apple Account linking is fire-and-forget after that.
    private func completeInitialize(call: CAPPluginCall) {
        print("SquareTapToPay: completeInitialize - awaiting location permission before resolving")
        ensureLocationPermission { [weak self] granted, _ in
            guard let self = self else { return }
            print("SquareTapToPay: completeInitialize - location permission granted=\(granted), resolving initialize()")

            let tapSettings = MobilePaymentsSDK.shared.readerManager.tapToPaySettings
            print("SquareTapToPay: === TAP TO PAY DIAGNOSTICS ===")
            print("SquareTapToPay: isDeviceCapable = \(tapSettings.isDeviceCapable)")
            tapSettings.isAppleAccountLinked { linked, _ in
                print("SquareTapToPay: isAppleAccountLinked = \(linked)")
                if !linked {
                    print("SquareTapToPay: Apple account NOT linked — calling linkAppleAccount()...")
                    tapSettings.linkAppleAccount { linkError in
                        if let linkError = linkError {
                            print("SquareTapToPay: linkAppleAccount failed — \(linkError.localizedDescription), code=\((linkError as NSError).code), userInfo=\((linkError as NSError).userInfo)")
                        } else {
                            print("SquareTapToPay: linkAppleAccount succeeded!")
                        }
                    }
                }
            }

            let readers = MobilePaymentsSDK.shared.readerManager.readers
            print("SquareTapToPay: connected readers count = \(readers.count)")
            for reader in readers {
                print("SquareTapToPay: reader — model=\(reader.model)")
            }
            print("SquareTapToPay: === END DIAGNOSTICS ===")

            self.registerReaderObserverIfNeeded()
            self.linkAppleAccountIfNeeded()
            call.resolve([
                "status": "authorized",
                "locationGranted": granted
            ])
        }
    }

    /// NSError userInfo values aren't all JSON-serializable; coerce to strings
    /// so Capacitor can pass them through to the JS layer intact.
    private static func stringifyUserInfo(_ info: [String: Any]) -> [String: String] {
        var out: [String: String] = [:]
        for (k, v) in info {
            out[k] = String(describing: v)
        }
        return out
    }

    // ── isAvailable ─────────────────────────────────────────────────────────

    @objc func isAvailable(_ call: CAPPluginCall) {
        print("SquareTapToPay: isAvailable called, initialized=\(isInitialized)")
        // Web layer calls this on page load — before initialize() has run —
        // so we must answer without touching `.shared`.
        guard isInitialized else {
            call.resolve([
                "available": false,
                "isDeviceCapable": false,
                "reason": "SDK not initialized"
            ])
            return
        }

        // Square SDK access must happen on the main thread; the device
        // capability check reads UIKit state internally.
        DispatchQueue.main.async {
            // SDK 2.5.0 moved the device-capability check onto TapToPaySettings,
            // reachable through ReaderManager. isDeviceCapable returns true for
            // iPhone XS+ on iOS 16.7+.
            let supportsTapToPay = MobilePaymentsSDK.shared.readerManager
                .tapToPaySettings
                .isDeviceCapable
            call.resolve([
                "available": supportsTapToPay,
                "isDeviceCapable": supportsTapToPay
            ])
        }
    }

    // ── getAuthorizationState ───────────────────────────────────────────────

    @objc func getAuthorizationState(_ call: CAPPluginCall) {
        guard isInitialized else {
            call.resolve(["authorized": false])
            return
        }
        // Authorization state reads from shared SDK state that's mutated on
        // the main thread; query it there to avoid UI API thread violations.
        DispatchQueue.main.async {
            let state = MobilePaymentsSDK.shared.authorizationManager.state
            call.resolve(["authorized": state == .authorized])
        }
    }

    // ── startPayment ────────────────────────────────────────────────────────

    @objc func startPayment(_ call: CAPPluginCall) {
        guard isInitialized else {
            call.reject("Square SDK is not initialized. Call initialize() first.")
            return
        }
        guard let amountCents = call.getInt("amountCents") else {
            call.reject("amountCents is required")
            return
        }
        let currencyCode = call.getString("currencyCode") ?? "USD"
        let note = call.getString("note")
        print("SquareTapToPay: startPayment called, amount=\(amountCents)")

        // Square's SDK presents from a UIViewController and reads UIKit state
        // internally — both the auth state check and startPayment must run on
        // the main thread. Capacitor's bridge.viewController hosts the WKWebView.
        DispatchQueue.main.async { [weak self] in
            guard let self = self,
                  let presenter = self.bridge?.viewController else {
                call.reject("No host view controller available to present Square UI.")
                return
            }

            // Tap to Pay requires authorization first.
            let state = MobilePaymentsSDK.shared.authorizationManager.state
            print("SquareTapToPay: startPayment auth state = \(state)")
            guard state == .authorized else {
                call.reject("Square SDK is not authorized. Call initialize() first.")
                return
            }

            // Square's SDK requires CoreLocation authorization before each
            // Tap to Pay transaction; otherwise startPayment rejects with
            // "Location permission has not been granted to your application."
            // We block on ensureLocationPermission until the system prompt
            // fully resolves and the delegate fires — only then is
            // CLLocationManager.authorizationStatus stable enough for the
            // SDK's internal synchronous check inside startPayment.
            print("SquareTapToPay: startPayment - checking location permission before SDK call")
            self.ensureLocationPermission { [weak self] granted, errorMessage in
                guard let self = self else { return }
                print("SquareTapToPay: startPayment - location permission resolved, granted=\(granted)")
                if !granted {
                    call.reject(errorMessage ?? Self.locationDeniedMessage)
                    return
                }
                DispatchQueue.main.async {
                    print("SquareTapToPay: payment additionalMethods = tapToPay")
                    print("SquareTapToPay: tapToPaySettings.isDeviceCapable = \(MobilePaymentsSDK.shared.readerManager.tapToPaySettings.isDeviceCapable)")
                    // Reader pairing can lag the SDK authorize by ~30s. Wait
                    // briefly so we don't hand a half-ready SDK to Square's UI;
                    // if the reader still hasn't shown up after 10s proceed
                    // anyway — Square's own flow may trigger it.
                    self.waitForTapToPayReader(timeoutSeconds: 10.0) { _ in
                        self.beginPayment(
                            call: call,
                            amountCents: amountCents,
                            currencyCode: currencyCode,
                            note: note,
                            presenter: presenter
                        )
                    }
                }
            }
        }
    }

    /// Runs after the SDK + location checks pass. Always invoked on the main
    /// queue (the location helper dispatches its completion to main).
    private func beginPayment(
        call: CAPPluginCall,
        amountCents: Int,
        currencyCode: String,
        note: String?,
        presenter: UIViewController
    ) {
        // Map ISO 4217 → Square's currency enum. Default to USD; surface a
        // clear error if the caller passes something unsupported.
        let currency: Currency
        switch currencyCode.uppercased() {
        case "USD": currency = .USD
        case "CAD": currency = .CAD
        case "GBP": currency = .GBP
        case "AUD": currency = .AUD
        case "JPY": currency = .JPY
        case "EUR": currency = .EUR
        default:
            call.reject("Unsupported currency: \(currencyCode)")
            return
        }

        let amountMoney = Money(amount: UInt(amountCents), currency: currency)
        let params = PaymentParameters(
            paymentAttemptID: UUID().uuidString,
            amountMoney: amountMoney,
            processingMode: .onlineOnly
        )
        if let note = note { params.note = note }

        let promptParams = PromptParameters(
            mode: .default,
            additionalMethods: .all
        )

        self.pendingPaymentCall = call
        // Capacitor releases the call after the bridge method returns
        // unless we keep it alive. The Square delegate resolves it later.
        call.keepAlive = true

        let delegate = PaymentDelegateBridge(owner: self)
        self.paymentDelegate = delegate

        self.paymentHandle = MobilePaymentsSDK.shared.paymentManager.startPayment(
            params,
            promptParameters: promptParams,
            from: presenter,
            delegate: delegate
        )
        print("SquareTapToPay: payment started with default mode, all methods")
    }

    // ── Location permission ─────────────────────────────────────────────────

    /// Resolves with `(granted, errorMessage)` after the current
    /// CLLocationManager authorization status is known. If the status is
    /// `.notDetermined`, this triggers the system prompt and waits for the
    /// delegate callback before completing. Must be called on the main queue.
    private func ensureLocationPermission(completion: @escaping (Bool, String?) -> Void) {
        if locationManager == nil {
            locationManager = CLLocationManager()
        }
        guard let manager = locationManager else {
            print("SquareTapToPay: ensureLocationPermission - failed to create CLLocationManager")
            completion(false, "Unable to create CLLocationManager.")
            return
        }

        let status = manager.authorizationStatus
        print("SquareTapToPay: ensureLocationPermission - status = \(status.rawValue) (\(describeAuthStatus(status)))")
        switch status {
        case .authorizedAlways, .authorizedWhenInUse:
            print("SquareTapToPay: ensureLocationPermission - already granted, completing immediately")
            completion(true, nil)
        case .denied, .restricted:
            print("SquareTapToPay: ensureLocationPermission - denied/restricted, completing with error")
            completion(false, Self.locationDeniedMessage)
        case .notDetermined:
            print("SquareTapToPay: ensureLocationPermission - notDetermined, wiring delegate and requesting permission")
            let bridge = LocationDelegateBridge { [weak self] newStatus in
                guard let self = self else { return }
                print("SquareTapToPay: ensureLocationPermission - delegate fired, newStatus = \(newStatus.rawValue) (\(self.describeAuthStatus(newStatus)))")
                self.locationDelegate = nil
                switch newStatus {
                case .authorizedAlways, .authorizedWhenInUse:
                    completion(true, nil)
                case .denied, .restricted:
                    completion(false, Self.locationDeniedMessage)
                default:
                    completion(false, Self.locationDeniedMessage)
                }
            }
            self.locationDelegate = bridge
            manager.delegate = bridge
            manager.requestWhenInUseAuthorization()
            print("SquareTapToPay: ensureLocationPermission - requestWhenInUseAuthorization called, awaiting delegate")
        @unknown default:
            print("SquareTapToPay: ensureLocationPermission - unknown status, failing")
            completion(false, Self.locationDeniedMessage)
        }
    }

    private func describeAuthStatus(_ status: CLAuthorizationStatus) -> String {
        switch status {
        case .notDetermined: return "notDetermined"
        case .restricted: return "restricted"
        case .denied: return "denied"
        case .authorizedAlways: return "authorizedAlways"
        case .authorizedWhenInUse: return "authorizedWhenInUse"
        @unknown default: return "unknown"
        }
    }

    // ── presentSettings ─────────────────────────────────────────────────────

    /// Presents Square's built-in Mobile Payments SDK settings screen, which
    /// drives the one-time Tap to Pay on iPhone setup (Apple Account link,
    /// device pairing, etc.). After the sheet is dismissed we re-read the
    /// reader list so the logs show whether Tap to Pay registered itself.
    @objc func presentSettings(_ call: CAPPluginCall) {
        print("SquareTapToPay: presentSettings called")
        guard isInitialized else {
            call.reject("SDK not initialized")
            return
        }
        DispatchQueue.main.async { [weak self] in
            guard let self = self,
                  let presenter = self.bridge?.viewController else {
                call.reject("No view controller")
                return
            }
            MobilePaymentsSDK.shared.settingsManager.presentSettings(with: presenter) { error in
                if let error = error {
                    print("SquareTapToPay: presentSettings error: \(error.localizedDescription)")
                    call.reject(error.localizedDescription)
                    return
                }
                print("SquareTapToPay: presentSettings dismissed")
                let readers = MobilePaymentsSDK.shared.readerManager.readers
                print("SquareTapToPay: readers after settings: count=\(readers.count)")
                for reader in readers {
                    print("SquareTapToPay: reader model=\(reader.model)")
                }
                call.resolve()
            }
        }
    }

    // ── Reader observer ────────────────────────────────────────────────────

    /// Attaches our ReaderObserverBridge to the SDK so we get callbacks when
    /// the embedded Tap to Pay reader pairs / unpairs / changes state. The
    /// observer is held strongly on `self` (the SDK keeps a weak ref). Safe
    /// to call multiple times — we only register once.
    private func registerReaderObserverIfNeeded() {
        if readerObserver != nil { return }
        let bridge = ReaderObserverBridge(owner: self)
        self.readerObserver = bridge
        MobilePaymentsSDK.shared.readerManager.add(bridge)
        print("SquareTapToPay: ReaderObserver registered")
    }

    // ── Tap to Pay reader wait helper ──────────────────────────────────────

    /// Returns true if a Tap to Pay on iPhone reader (model rawValue 3) is
    /// currently present in the SDK's reader list.
    private func isTapToPayReaderConnected() -> Bool {
        let readers = MobilePaymentsSDK.shared.readerManager.readers
        return readers.contains { $0.model.rawValue == Self.tapToPayReaderModelRawValue }
    }

    /// Waits up to `timeoutSeconds` for a Tap to Pay reader to pair via the
    /// ReaderObserver. Resolves immediately if one is already connected, and
    /// resolves with `false` on timeout so the caller can proceed anyway —
    /// Square's UI sometimes triggers reader connection on its own.
    /// Must be called on the main queue.
    private func waitForTapToPayReader(
        timeoutSeconds: TimeInterval,
        completion: @escaping (Bool) -> Void
    ) {
        if isTapToPayReaderConnected() {
            print("SquareTapToPay: Tap to Pay reader already connected")
            completion(true)
            return
        }
        print("SquareTapToPay: waiting up to \(timeoutSeconds)s for Tap to Pay reader to connect")
        var resolved = false
        self.readerWaitCompletion = { [weak self] connected in
            guard !resolved else { return }
            resolved = true
            self?.readerWaitCompletion = nil
            print("SquareTapToPay: reader wait resolved, connected=\(connected)")
            completion(connected)
        }
        DispatchQueue.main.asyncAfter(deadline: .now() + timeoutSeconds) { [weak self] in
            if resolved { return }
            resolved = true
            self?.readerWaitCompletion = nil
            print("SquareTapToPay: reader wait timed out; proceeding anyway")
            completion(false)
        }
    }

    /// Called by ReaderObserverBridge when readerWasAdded fires. Fulfills the
    /// pending startPayment() wait if the new reader is the Tap to Pay reader.
    fileprivate func handleReaderAdded(_ readerInfo: ReaderInfo) {
        guard readerInfo.model.rawValue == Self.tapToPayReaderModelRawValue else { return }
        if let waiter = self.readerWaitCompletion {
            waiter(true)
        }
    }

    /// Square requires the merchant to link an Apple Account (accept Apple's
    /// Tap to Pay T&Cs) before Tap to Pay payments will run. Fire-and-forget:
    /// initialize() has already resolved, and if linking fails the first
    /// startPayment() will surface the underlying SDK error.
    private func linkAppleAccountIfNeeded() {
        DispatchQueue.main.async {
            let settings = MobilePaymentsSDK.shared.readerManager.tapToPaySettings
            settings.isAppleAccountLinked { linked, error in
                print("SquareTapToPay: Apple account linked = \(linked)")
                if let error = error {
                    let ns = error as NSError
                    print("SquareTapToPay: isAppleAccountLinked error domain=\(ns.domain) code=\(ns.code) message=\(ns.localizedDescription)")
                }
                if linked { return }
                DispatchQueue.main.async {
                    print("SquareTapToPay: linking Apple account...")
                    settings.linkAppleAccount { linkError in
                        if let linkError = linkError {
                            let ns = linkError as NSError
                            print("SquareTapToPay: linkAppleAccount error domain=\(ns.domain) code=\(ns.code) message=\(ns.localizedDescription)")
                        } else {
                            print("SquareTapToPay: Apple account linked successfully")
                        }
                    }
                }
            }
        }
    }

    // ── Delegate callbacks (bridged in from PaymentDelegateBridge) ─────────

    fileprivate func handleDidFinish(_ payment: Payment) {
        guard let call = pendingPaymentCall else { return }
        defer { resetPaymentState() }

        // Pull card brand + last4 when available (online card payments).
        // In SDK 2.5.0, card info hangs off OnlinePayment.cardDetails.card
        // (CardPaymentDetails → Card), not the old sourceType.card path.
        var cardBrand: String?
        var last4: String?
        if let card = (payment as? OnlinePayment)?.cardDetails?.card {
            cardBrand = describeBrand(card.cardBrand)
            last4 = card.last4
        }

        // localID lives on OfflinePayment only in 2.5.0; Tap to Pay is
        // online-only, so there's no client-side ID to surface here.
        call.resolve([
            "status": "success",
            "transactionId": payment.id ?? "",
            "clientTransactionId": "",
            "cardBrand": cardBrand ?? "",
            "last4": last4 ?? ""
        ])
    }

    fileprivate func handleDidFail(_ payment: Payment?, error: Error) {
        guard let call = pendingPaymentCall else { return }
        defer { resetPaymentState() }
        let ns = error as NSError
        print("SquareTapToPay: payment error - \(ns.localizedDescription), code: \(ns.code), userInfo: \(ns.userInfo)")
        call.resolve([
            "status": "error",
            "errorMessage": ns.localizedDescription,
            "errorCode": ns.code,
            "errorDomain": ns.domain,
            "errorUserInfo": Self.stringifyUserInfo(ns.userInfo)
        ])
    }

    fileprivate func handleDidCancel(_ payment: Payment) {
        guard let call = pendingPaymentCall else { return }
        defer { resetPaymentState() }
        call.resolve(["status": "cancelled"])
    }

    private func resetPaymentState() {
        pendingPaymentCall = nil
        paymentHandle = nil
        paymentDelegate = nil
    }

    // SDK 2.5.0 promoted Card.Brand to a top-level CardBrand enum and renamed
    // the JCB case to .jcb (was .JCB). New cases like .squareCapitalCard and
    // .otherBrand are handled by @unknown default.
    private func describeBrand(_ brand: CardBrand) -> String {
        switch brand {
        case .visa: return "VISA"
        case .mastercard: return "MASTERCARD"
        case .americanExpress: return "AMERICAN_EXPRESS"
        case .discover: return "DISCOVER"
        case .discoverDiners: return "DISCOVER_DINERS"
        case .jcb: return "JCB"
        case .chinaUnionPay: return "CHINA_UNION_PAY"
        case .squareGiftCard: return "SQUARE_GIFT_CARD"
        case .interac: return "INTERAC"
        case .eftpos: return "EFTPOS"
        case .felica: return "FELICA"
        case .ebt: return "EBT"
        @unknown default: return "OTHER"
        }
    }
}

// ── PaymentManagerDelegate bridge ─────────────────────────────────────────
// Square's delegate methods are NSObjectProtocol-based and we don't want the
// plugin class itself to conform (keeps the public surface clean). This thin
// bridge forwards every callback to the plugin.

fileprivate final class PaymentDelegateBridge: NSObject, PaymentManagerDelegate {
    weak var owner: SquareTapToPayPlugin?

    init(owner: SquareTapToPayPlugin) {
        self.owner = owner
        super.init()
    }

    func paymentManager(_ paymentManager: PaymentManager, didFinish payment: Payment) {
        owner?.handleDidFinish(payment)
    }

    func paymentManager(_ paymentManager: PaymentManager, didFail payment: Payment, withError error: Error) {
        owner?.handleDidFail(payment, error: error)
    }

    func paymentManager(_ paymentManager: PaymentManager, didCancel payment: Payment) {
        owner?.handleDidCancel(payment)
    }
}

// ── ReaderObserver bridge ─────────────────────────────────────────────────
// Square's ReaderManager pushes reader lifecycle events to a weakly-held
// observer. We forward each callback to the console so we can see — during
// Tap to Pay setup — whether the embedded reader auto-pairs, gets removed,
// or changes status (firmware update, card insertion, etc.).

fileprivate final class ReaderObserverBridge: NSObject, ReaderObserver {
    weak var owner: SquareTapToPayPlugin?

    init(owner: SquareTapToPayPlugin) {
        self.owner = owner
        super.init()
    }

    func readerWasAdded(_ readerInfo: ReaderInfo) {
        print("SquareTapToPay: readerWasAdded — model=\(readerInfo.model) (rawValue=\(readerInfo.model.rawValue)) name=\(readerInfo.name) connection=\(readerInfo.connectionType)")
        owner?.handleReaderAdded(readerInfo)
    }

    func readerWasRemoved(_ readerInfo: ReaderInfo) {
        print("SquareTapToPay: readerWasRemoved — model=\(readerInfo.model) name=\(readerInfo.name)")
    }

    func readerDidChange(_ readerInfo: ReaderInfo, change: ReaderChange) {
        print("SquareTapToPay: readerDidChange — model=\(readerInfo.model) change=\(change)")
    }
}

// ── CLLocationManagerDelegate bridge ──────────────────────────────────────
// CLLocationManagerDelegate is iOS-14+. We fire `onResolved` once the
// authorization status leaves `.notDetermined`. The initial delegate-set
// callback (which can carry `.notDetermined`) is ignored.

fileprivate final class LocationDelegateBridge: NSObject, CLLocationManagerDelegate {
    private let onResolved: (CLAuthorizationStatus) -> Void
    private var fired = false

    init(onResolved: @escaping (CLAuthorizationStatus) -> Void) {
        self.onResolved = onResolved
        super.init()
    }

    func locationManagerDidChangeAuthorization(_ manager: CLLocationManager) {
        let status = manager.authorizationStatus
        guard status != .notDetermined, !fired else { return }
        fired = true
        onResolved(status)
    }
}

